let son1 = Number(prompt("Birinchi sonni kriting"));
let son2 = Number(prompt("Icinchi sonni kriting"));
let amal = String(prompt("Amalni kiriting (+, -, *, /): "));

if (amal === "+"); {
    let yigindi = son1 + son2;
    alert(son1 + " + " + son2 + " = " + yigindi);
}

if (amal === "-"); {
    let yigindi = son1 - son2;
    alert(son1 + " - " + son2 + " = " + yigindi);
}

if (amal === "*"); {
    let yigindi = son1 * son2;
    alert(son1 + " * " + son2 + " = " + yigindi);
}

if (amal === "/"); {
    let yigindi = son1 + son2;
    alert(son1 + " / " + son2 + " = " + yigindi);
}